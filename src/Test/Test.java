    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        jTextArea1.setText(" ");
        System.getProperty("java.classpath");
        if (seleccionar.showDialog(null, "Guardar") == JFileChooser.APPROVE_OPTION) {
            
            archivo = seleccionar.getSelectedFile();
            if (archivo.canRead()) {
                if (archivo.getName().endsWith("jpg") || archivo.getName().endsWith("png") || archivo.getName().endsWith("jpeg") || 
                        archivo.getName().endsWith("JPG") || archivo.getName().endsWith("PNG") || archivo.getName().endsWith("JPEG")) {
                    System.out.println(archivo.getPath());
                    img = abrirArchivo(archivo);
                    jTextField1.setText(archivo.getName());
                    lblMostrar.setIcon(new ImageIcon(archivo.getPath()));
                    nameArchivo = archivo.getPath();
                } else {
                    JOptionPane.showMessageDialog(null, "Archivo no compatible");
                }
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

